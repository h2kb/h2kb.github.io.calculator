package io.github.h2kb.calculator;

class Operators {

    static int add(int num1, int num2) {
        return num1 + num2;
    }

    static int sub(int num1, int num2) {
        return num1 - num2;
    }

    static int multi(int num1, int num2) {
        return num1 * num2;
    }

    static int div(int num1, int num2) {
        return num1 / num2;
    }
}
